def resnet18(*, weights = None, progress: bool = True, se_reduction: int = 16, **kwargs: Any) -> ResNet:
    """ResNet-18 with Squeeze-and-Excitation blocks."""
    return _resnet(BasicBlock, [2, 2, 2, 2], None, progress, se_reduction=se_reduction, **kwargs)


def MainModel(nOut=256, **kwargs):
    """
    Entry point used by EmbedNet for SE-ResNet18. Keeps original parameter names so pretrained
    weights can load; SE layers are new and will initialize randomly.
    """
    # Allow overriding reduction but ignore other training-time kwargs
    se_reduction = kwargs.pop('se_reduction', 16)
    return resnet18(num_classes=nOut, se_reduction=se_reduction)
